a = 20
b = 10

print('------------greater than---------------')
print(a > b)
print(b > a)

print('------------smaller than---------------')
print(a < b)
print(b < a)

print('------------equals---------------')
print(a == b)
print(b == a)

print('------------does not equal---------------')
print(a != b)
print(b != a)

print('------------smaller than or equal to---------------')
print(a <= b)
print(b <= a)

print('------------greater than or equal to---------------')
print(a >= b)
print(b >= a)

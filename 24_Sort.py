# sort
a = ("a", "h", "k", "e", "r", 'A', 'Z', 'K')
x = sorted(a)
print(x)
# sorted()
a = (1, 11, 2, 12, 5, 7, 8)
x = sorted(a)
print(x)
# Sort ascending:
a = ("h", "b", "a", "c", "f", "d", "e", "g")
x = sorted(a)
print(x)
# Sort descending:
a = ("h", "b", "a", "c", "f", "d", "e", "g")
x = sorted(a, reverse=True)
print(x)
